DROP DATABASE if exists EKART;
CREATE DATABASE EKART;
USE  EKART;


CREATE TABLE EK_PRODUCT_CATEGORY( 
CATEGORY VARCHAR(50) NOT NULL, 
constraint EK_PROD_CATEGORY_PK primary key ( CATEGORY )
);

CREATE TABLE EK_SELLER(
	EMAIL_ID VARCHAR(50),
	NAME VARCHAR(50) NOT NULL,
	PASSWORD VARCHAR(70) NOT NULL,
	PHONE_NUMBER VARCHAR(10) NOT NULL UNIQUE,
	ADDRESS VARCHAR (500) NOT NULL,
	constraint EK_SELLER_EMAIL_ID_PK primary key ( EMAIL_ID )
);

CREATE TABLE EK_PRODUCT(
	PRODUCT_ID INT auto_increment,
	NAME VARCHAR(500) NOT NULL,
	TYPE VARCHAR(250) NOT NULL,
	CATEGORY VARCHAR(200) NOT NULL,
	MODEL VARCHAR(250) NOT NULL,
	PRICE DOUBLE NOT NULL,
	QUANTITY SMALLINT NOT NULL,
    SELLER_EMAIL_ID VARCHAR(50),
	constraint EK_PRODUCT_ID_PK primary key ( PRODUCT_ID )
);

CREATE TABLE EK_CUSTOMER(
	EMAIL_ID VARCHAR(50),
	NAME VARCHAR(50) NOT NULL,
	PASSWORD VARCHAR(70) NOT NULL,
	PHONE_NUMBER VARCHAR(10) NOT NULL UNIQUE,
	constraint EK_CUSTOMER_EMAIL_ID_PK primary key ( EMAIL_ID )
);

